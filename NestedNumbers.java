public class NestedNumbers {
    
    public static void main(String[] args) {
             
        for (int lines = 1; lines <= 4; lines++) {
                      
            for (int num = 9; num >= 1; num--) {
                
                for (int rep = 9; rep >= 1; rep--) {
                   
                   
                    System.out.print(num);
                }
                
            }
            
            System.out.println();
        }
    }
}